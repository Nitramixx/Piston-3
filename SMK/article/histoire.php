			<div class="article">
				<h2 class="titre-article">De l'histoire des SUF à la création de la troupe</h2>

				<div class="informations">
				<p class='histoire-scoutisme'>Le scoutisme est un mouvement de jeunesse créé il y a plus de 100 ans par lord Robert Baden-Powell 
					en Angleterre et adapté au catholicisme en France par le père Jacques Sevin. En voici l’histoire 
					de sa création jusqu'à la fondation de la troupe de Saint Maximilien Kolbe, 
					en passant par l'apparition du mouvement des Scouts Unitaires de France, 
					et par la vie de Maximilien Kolbe.
				</p>

				<h3>La création du scoutisme</h3>
				<div class='histoire-scoutisme'>
					<h4>La génèse</h4>
				<p>
					Le scoutisme est né en 1907 sur l’île de Brownsea tout au sud de l’Angleterre. 
					Il a vu le jour au milieu d’un camp de 20 garçons, organisé par le héros de guerre
					<a href="dictionnaire.php#Baden-Powell" target="_blank" class="a-definir">Baden-Powell</a>, alias BP!
					Ce camp fut un succès : les jeunes appréciaient les méthodes de formation proposées, qui de plus étaient efficaces. 
					Ainsi, en 1908 il publie la première édition de 
					<a href="dictionnaire.php#Eclaireurs" target="_blank" class="a-definir"><cite>"Eclaireurs" (titre original : Scouting for Boys)</cite></a>
					, qui fut vendu à plus de 100 millions d’exemplaires ! 
					Il développe dans ce livre les méthodes de formation qu’il met en pratique et celles-ci rencontrent rapidement 
					un franc succès notamment auprès des jeunes eux-mêmes. 
				</p>
				<p>
					En 1909, le mouvement des <span lang='en'>"Boy Scouts"</span> était né et un rassemblement scout à Londres attira déjà plus de 11.000
					garçons ! En plus de l’Angleterre, le scoutisme s’implanta rapidement au Chili, où habita Baden-Powell, 
					mais aussi au Canada et aux Etats-Unis. Le mouvement officiel des Scouts de France est créé en 1920 par le père jésuite
					Jacques Sevin et le chanoine Cornette, même si quelques troupes avaient déjà vu le jour auparavant.
				</p>
				<h4>La première Guerre Mondiale</h4>
				<p>
					Pendant la Première Guerre Mondiale, les chefs de patrouille remplacèrent les responsables adultes partis combattre. 
					Ils contribuèrent même à l’effort de guerre, à l’image des scouts marins qui prirent, en Angleterre, 
					le rôle de garde-côtes par exemple.
				</p>
				<h4>L'expansion du mouvement</h4>
				<p>
					En 1920 a lieu le premier <a href="dictionnaire.php#jamboree" target="_blank" class="a-definir">Jamboree Scout Mondial </a>
					qui réunit près de 8.000 participants à Londres. 
					21 autres Jamboree auront lieu à travers le monde. C’est à ce Jamboree de Londres que fut fondé le 
					<a href="dictionnaire.php#BMS" target="_blank" class="a-definir">Bureau Mondial du Scoutisme </a>
					, dans le cadre de la première 
					<a href="dictionnaire.php#CMS2" target="_blank" class="a-definir">Conférence Mondiale du Scoutisme </a>.
					2 ans plus tard, le premier 
					<a href="dictionnaire.php#CMS1" target="_blank" class="a-definir">Comité Mondial du Scoutisme</a>
					fut élu au cours de la deuxième Conférence Internationale organisée à Paris. Le Mouvement mondial 
					comptait alors juste un peu plus de un million de membres.
				</p>
				<p>
					Si le scoutisme fut créé pour les jeunes garçons de 11 à 18 ans, les filles, 
					les plus jeunes et les plus âgés voulurent vite y participer. C’est ainsi que Baden-Powell lança le programme 
					des Guides en 1910 avec l’aide de son épouse Olave. 
					On créa pour les plus jeunes la branche des Louveteaux, avec un imaginaire centré sur le <cite>Livre de la Jungle </cite>
					de Rudyard Kipling.	Pour les plus âgés, la branche des Routiers fut créée.
				</p>
				</div>

				<h3>La fondation du mouvement des Scouts Unitaires de France</h3>
				<div class='histoire-scoutisme'>
					<h4>1965</h4>
					<p>
					Les années précédentes, plusieurs expériences ont eu lieu pour moderniser le scoutisme, et l’adapter à l’époque 
					qui change à tout allure ! François Lebouteux propose alors de diviser les troupes en deux : 
					Aux Rangers en chemise bleues (de 12 à 14 ans) reviennent les jeux. 
					Aux Pionniers en chemises rouges (de 15 à 18 ans) reviennent l’engagement dans la cité.
					</p>
					<h4>La fracture</h4>
					<p>
					Toutefois, certains, comme Pierre Delsuc, Michel Menu ou Henri Dhavermas, y trouvent des inconvénients 
					: la fracture de la patrouille fait que les plus jeunes ont moins envie de progresser et les plus âgés 
					sont moins responsables. De plus cette vision du scoutisme s’éloigne de la nature. 
					Ainsi, certaines troupes rejoignent les Scouts d’Europe , arrivés d’Autriche en 1958. 
					D’autres, que l’on appelle « unitaires », décident de garder l’unité de la patrouille 
					avec des jeunes de 12 à 17 ans. 
					</p>
					<h4>La naissance des SUF</h4>
					<p>
					Cette différence devenant impossible à gérer, le centre national fait pression sur ces troupes pour qu’elles
					adoptent la réforme. Mais le 13 février 1971, 55 chefs venus de toute la France se réunissent et fondent
					le mouvement des Scouts Unitaires de France autour du premier aumônier SUF : le chanoine Ancel.
					</p>
					<h4>Les SUF aujourd'hui</h4>
					<p>
					Cinq cents en 1971, dix mille en 1980, plus de vingt-cinq mille aujourd’hui. 
					Le scoutisme, c’est comme la graine de moutarde… Il suffit de le laisser pousser ! 
					Certes, les scoutisme est divisé, mais il est vivant, et même plus que jamais. 
					Les tempêtes ne manquent pas, mais les fils de Baden-Powell ont la vie dure. 
					Les SUF sont un pue partout en France et même à l’étranger. 
					Belles troupes anciennes ou jeunes troupes fraîchement créées, patrouilles libres et scouts marins… 
					Depuis 1973, les filles ont rejoint l’association. Les meutes se multiplient, la Route a été recréée.
					</p>
					<p>
					En 2007, 13.000 SUF se retouvent à Lyon pour célébrer le Jubilé. 
					Un nouveau rassemblement unit tous les louveteaux, jeanettes, éclaireurs, guides, routiers et guides-aînées, 
					à la Pentecôte 2007 pour fêter le centenaire du scoutisme. A leur tour les SUF donnent des prêtres, 
					des entrepreneurs, des ministres, des pères et des mères de famille, des hommes solides et droits, 
					fiers de leur foi, joyeux, simples et dévoués. Chez les SUF, on aime ce qui est beau, ce qui est simple, 
					ce qui est joyeux. On croit que l’aventure , l’amitié et la foi vont ensemble.
					</p>
				</div>


